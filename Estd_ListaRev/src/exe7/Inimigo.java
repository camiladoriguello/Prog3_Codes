package exe7;

public class Inimigo {
	
	//----------Attributes----------
	private String nome;
	private int dificuldade;

	//----------Getters and Setters----------
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getDificuldade() {
		return dificuldade;
	} 
	public void setDificuldade(int dificuldade) {
		this.dificuldade = dificuldade;
	}
	
}
